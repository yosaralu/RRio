#Challenge 2 calculate the average life exp per country. 
#which had the longest life exp and which had the shortest? 

gapminder_mean_lifeExp <- gapminder %>%
  group_by(country) %>%
  summarize(mean_lifeExp = mean(lifeExp)) %>%
